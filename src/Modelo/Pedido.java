package Modelo;

import java.util.Date;

/**
 *
 * @author USUARIO
 */
public class Pedido {
    
    private int id;
    private Date fecha;
    private float total;

    public Pedido() {}

    public Pedido(int id, Date fecha, float total) {
        this.id = id;
        this.fecha = fecha;
        this.total = total;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public float getTotal() {
        return total;
    }

    public void setTotal(float total) {
        this.total = total;
    }
    
}
